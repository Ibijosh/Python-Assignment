#2. Write a program that calculates the sum of the first 10 natural numbers (1 to 10) using while loop.

i = 1  # Initializing counter variable
a = 0  # Initializing sum variable
while i <= 10: # Loop through numbers 1 to 10
     a += i  # Add current number to sum
     i += 1  # Increment counter
     print(a)